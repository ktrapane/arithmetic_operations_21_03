package com.company;

public class Main {

    public static void main(String[] args) {
	// Program that performs all artimetich operations and prints the result to the console
        int val1 = 12, val2 = 5;

        //performing adding, adding val1 to val2
        System.out.println("val1 + val2 = " + (val1 + val2) );

        //performing subtraction, subtract val1 to val2
        System.out.println("val1 - val2 = " + (val1-val2) );

        //performing multiplication, multiply val1 from val2
        System.out.println("val1 * val2 = " + (val1*val2) );

        //performing division, divide val2 from val1
        System.out.println("val1 / val2 = " + (val1/val2) );

        //Performing modulo operations, reminder after divison
        System.out.println("val1 % val2 = " + (val1 % val2));

    }
}
